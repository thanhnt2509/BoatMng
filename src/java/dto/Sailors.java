package dto;

import java.io.Serializable;

/**
 *
 * @author Legion
 */
public class Sailors implements Serializable{
    private int sid;
    private String sName;
    private String password;
    private int rating;
    private int age;

    public Sailors() {
    }

    public Sailors(int sid, String sName, String password, int rating, int age) {
        this.sid = sid;
        this.sName = sName;
        this.password = password;
        this.rating = rating;
        this.age = age;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
}
